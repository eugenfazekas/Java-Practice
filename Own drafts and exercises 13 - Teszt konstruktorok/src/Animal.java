
public class Animal {

	private String name;
	
	private int weight;
	
	private int age;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}
	
	int getAge() {
		
		return this.age;
	}
	
	void setAge (int age) {
		
		this.age = age;
	}
}
